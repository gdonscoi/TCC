package br.com.uniararas.beans;

public class Materias {

	private String nomemateria;
	
	private String primeiranota;
	
	private String notaspa;
	
	private String segundanota;
	
	private String mediafinal;

	private String numerofaltas;
	
	private String numerofaltaslimite;

	public String getNomemateria() {
		return nomemateria;
	}

	public void setNomemateria(String nomemateria) {
		this.nomemateria = nomemateria;
	}

	public String getPrimeiranota() {
		return primeiranota;
	}

	public void setPrimeiranota(String primeiranota) {
		this.primeiranota = primeiranota;
	}

	public String getNotaspa() {
		return notaspa;
	}

	public void setNotaspa(String notaspa) {
		this.notaspa = notaspa;
	}

	public String getSegundanota() {
		return segundanota;
	}

	public void setSegundanota(String segundanota) {
		this.segundanota = segundanota;
	}

	public String getMediafinal() {
		return mediafinal;
	}

	public void setMediafinal(String mediafinal) {
		this.mediafinal = mediafinal;
	}

	public String getNumerofaltas() {
		return numerofaltas;
	}

	public void setNumerofaltas(String numerofaltas) {
		this.numerofaltas = numerofaltas;
	}

	public String getNumerofaltaslimite() {
		return numerofaltaslimite;
	}

	public void setNumerofaltaslimite(String numerofaltaslimite) {
		this.numerofaltaslimite = numerofaltaslimite;
	}
	
}
